'''
Write a simple script to demonstrate your understanding of tuples as data types 
'''
tup = (2, 5, "name", 8, 10)

tup1 = (12, 22, 15)

print(tup) # give the output  always in paranthesis.

print(tup[2]) # print the third value in a tuple

print(tup[1:]) # it gives an output of tuple with second to last item.

print(max(tup1)) # it gives the maximum value in the tuple.

print(min(tup1)) # it gives the minimum value in the tuple.