''' 
Write a simple script that demonstrate your understanding of conditional statements 
Simple If , if else , elif  
'''
num = float(input("Enter a number: ")) # user input.

if num > 0: # check if the number is positive.

    print("Positive number") # output

elif num == 0: # check if the number is zero.

    print("Zero") # output.

else:
    print("Negative number") # otherwise the number will be negative. 